#ifndef CAN_RECEIVE_H
#define CAN_RECEIVE_H

#include "struct_typedef.h"
#include "can.h"
#include "pid.h"

#define CHASSIS_CAN hcan1
#define GIMBAL_CAN hcan2

/* CAN send and receive ID */
typedef enum
{
    CAN_CHASSIS_ALL_ID = 0x200,
    CAN_3508_M1_ID = 0x201,
    CAN_3508_M2_ID = 0x202,
    CAN_3508_M3_ID = 0x203,
    CAN_3508_M4_ID = 0x204,

    CAN_YAW_MOTOR_ID = 0x205,
    CAN_PIT_MOTOR_ID = 0x206,
    CAN_TRIGGER_MOTOR_ID = 0x207,
    CAN_GIMBAL_ALL_ID = 0x1FF,

} can_msg_id_e;

//rm motor data
typedef struct
{
    uint16_t ecd;
    int16_t speed_rpm;
    int16_t given_current;
    uint8_t temperate;
    int16_t last_ecd;
} motor_measure_t;

typedef struct _Motor
{
    //是否使能电机
    int enable_motor;
    
    //电机的绝对编码器范围，0 ~ ecd_range.
    int ecd_range;
    
    //电机当前编码器的值
    int ecd;
    
    //电机当前的速度
    float speed;
    
    //电机上次的编码器值
    int offset_ecd;
    
    //电机当前的总编码器值
    int current_position;
    
    //电机数据指针
    const motor_measure_t *motor_measure;
    
    //设置电机的电流
    int16_t current;
    
    //电机编码器的目标值
    int target_position;
    
    //电机速度的目标值
    float target_speed;
    
    //电机速度环的pid
    pid_type_def speed_pid;
    
    //电机位置环的pid
    pid_type_def position_pid;
    
    
    
} Motor;

typedef struct _MotorGroup
{
    //是否使能电机组
    int enable_group;
    
    //电机组对应的can口
    CAN_HandleTypeDef *hcan_ptr;
    
    //定义电机id是1234还是5678
    uint32_t motor_id;
    
    //四个电机结构体
    Motor motor[4];
    
} MotorGroup;

/**
  * @brief          send control current of motor (0x205, 0x206, 0x207, 0x208)
  * @param[in]      yaw: (0x205) 6020 motor control current, range [-30000,30000] 
  * @param[in]      pitch: (0x206) 6020 motor control current, range [-30000,30000]
  * @param[in]      shoot: (0x207) 2006 motor control current, range [-10000,10000]
  * @param[in]      rev: (0x208) reserve motor control current
  * @retval         none
  */
/**
  * @brief          发送电机控制电流(0x205,0x206,0x207,0x208)
  * @param[in]      yaw: (0x205) 6020电机控制电流, 范围 [-30000,30000]
  * @param[in]      pitch: (0x206) 6020电机控制电流, 范围 [-30000,30000]
  * @param[in]      shoot: (0x207) 2006电机控制电流, 范围 [-10000,10000]
  * @param[in]      rev: (0x208) 保留，电机控制电流
  * @retval         none
  */
extern void CAN_cmd_gimbal(int16_t yaw, int16_t pitch, int16_t shoot, int16_t rev);

/**
  * @brief          send CAN packet of ID 0x700, it will set chassis motor 3508 to quick ID setting
  * @param[in]      none
  * @retval         none
  */
/**
  * @brief          发送ID为0x700的CAN包,它会设置3508电机进入快速设置ID
  * @param[in]      none
  * @retval         none
  */
extern void CAN_cmd_chassis_reset_ID(void);

/**
  * @brief          send control current of motor (0x201, 0x202, 0x203, 0x204)
  * @param[in]      motor1: (0x201) 3508 motor control current, range [-16384,16384] 
  * @param[in]      motor2: (0x202) 3508 motor control current, range [-16384,16384] 
  * @param[in]      motor3: (0x203) 3508 motor control current, range [-16384,16384] 
  * @param[in]      motor4: (0x204) 3508 motor control current, range [-16384,16384] 
  * @retval         none
  */
/**
  * @brief          发送电机控制电流(0x201,0x202,0x203,0x204)
  * @param[in]      motor1: (0x201) 3508电机控制电流, 范围 [-16384,16384]
  * @param[in]      motor2: (0x202) 3508电机控制电流, 范围 [-16384,16384]
  * @param[in]      motor3: (0x203) 3508电机控制电流, 范围 [-16384,16384]
  * @param[in]      motor4: (0x204) 3508电机控制电流, 范围 [-16384,16384]
  * @retval         none
  */
extern void CAN_cmd_chassis(int16_t motor1, int16_t motor2, int16_t motor3, int16_t motor4);

/**
  * @brief          让电机组所属电调中的所有电机进入快速设置ID模式
  * @param[in]      none
  * @retval         none
  */
extern void cmd_group_reset_ID(MotorGroup* motor_group);

/**
  * @brief          给电机组中的电机发送电流控制指令
  * @param[in]      motor_group: 电机组的指针
  * @retval         none
  */
extern void cmd_motor_group(MotorGroup* motor_group);

/**
  * @brief          返回电机数据指针
  * @param[in]      motor_group: 电机组指针
  * @param[in]      motor_id: 电机的电调ID ( 1-8 )
  * @retval         电机数据指针
  */
extern const motor_measure_t *get_motor_measure_point(MotorGroup* motor_group, int motor_id);



/**
  * @brief          return the chassis 3508 motor data point
  * @param[in]      i: motor number,range [0,3]
  * @retval         motor data point
  */
/**
  * @brief          返回底盘电机 3508电机数据指针
  * @param[in]      i: 电机编号,范围[0,3]
  * @retval         电机数据指针
  */
extern const motor_measure_t *get_chassis_motor_measure_point(uint8_t i);


#endif
